                                <div class="entry-content"><p><img class="right" src="/images/2013-05-10-devopsreport/devopsreport.png" width="200">
                                    Those of us who have been aligned with DevOps for some time already know that the greater agility and closer collaboration it enables deliver real business value for our organizations.</p>

                                    <p>But until recently, our knowledge was based largely on our personal experience, and that of our friends and colleagues. So <a href="http://puppetlabs.com/">Puppet Labs</a> and <a href="http://itrevolution.com/">IT Revolution Press</a> partnered late last year to survey sysadmins, developers and other technology professionals in search of hard data that would tell us whether DevOps really does deliver solid results.<!--More--></p>

                                    <p>The data we gathered - summed up in <a href="http://info.puppetlabs.com/2013-state-of-devops-report.html">2013 State of DevOps Report</a> - confirmed what we knew instinctively:  DevOps makes companies of any size more agile - and more productive.</p>

                                    <p>We also found that organizations using DevOps practices pay their people more. This was true across technical teams at companies of all sizes.</p>

                                    <p>We believe that, taken together, the data on higher productivity and higher wages for DevOps people show that companies recognize the business value DevOps delivers. We also believe this recognition is driving the acceleration of DevOps adoption.</p>

                                    <h2>DevOps Knows No Borders</h2>

                                    <p>Our survey tapped more than 4,000 IT ops people, developers and QA engineers living in more than 90 countries. They work for a wide spread of organizations - startups, small to medium-sized companies, and huge corporations.</p>

                                    <p>About 80 percent of our respondents are hands-on: sysadmins, developers or engineers. Break this down further, and we see more than two thirds of these hands-on folks are in IT ops. The other 30 percent work in development and engineering.</p>

                                    <h2>DevOps orgs ship faster, with fewer fails</h2>

                                    <p>DevOps teams are more productive than conventional IT and engineering organizations, measured by two important key performance metrics:</p>

                                    <ul>
                                        <li>Faster deployment. Code is shipped 30 times more frequently: one or more times per day, instead of once a month - or once a year. Deployments are completed 8,000 times faster.</li>
                                        <li>Far fewer service interruptions. Change failure drops by 50 percent, and service is restored 12 times faster.</li>
                                    </ul>


                                    <p>The combination of more rapid deployment and fewer failures means DevOps teams deliver more business value, and give their companies a competitive edge. Interestingly, we found teams that have been using DevOps practices the longest reported the fastest deployments and the highest success rates. To cite just a few high-profile examples, Google, Amazon, Twitter and Etsy are all known for deploying frequently, without disrupting service to their customers.</p>

                                    <h2>Version control + deployment automation = higher productivity, lower costs &amp; quicker wins</h2>

                                    <p>Our survey identified two key DevOps practices that enable improved performance. Of the highest-performing organizations:</p>

                                    <ul>
                                        <li>89 percent use version control systems for infrastructure management</li>
                                        <li>82 percent automate their code deployments</li>
                                    </ul>


                                    <p>Version control allows you to quickly identify what’s failed, and why, so you can quickly roll your system back to the last known good state. Deployment automation eliminates configuration drift between environments. Using both means your team saves time and reduces errors by replacing manual workflows with a consistent and repeatable process.</p>

                                    <p>The result is consistent technical performance. Your management team can count on that consistency - and as a bonus, your team is freed up to concentrate on the innovations that deliver your company a real competitive advantage.</p>

                                    <h2>DevOps organizations pay their people better</h2>

                                    <p>Our survey findings indicate that technical teams identifying DevOps skills and experience as important for their hiring process pay their people more.</p>

                                    <p>Half of our respondents said that their teams consider DevOps experience when hiring. Of these respondents, 42 percent make $100,000 per year, or more. Survey respondents on teams that don’t consider DevOps experience aren’t quite so well paid - just 29 percent of them make $100,000 or more.</p>

                                    <p>It makes sense to us that DevOps teams are better compensated. The higher level of productivity DevOps delivers - and the resulting business value - justifies higher salaries. And paying more could be seen as risk mitigation - people with DevOps skills are just too valuable to leave out in the wild, where they could be snapped up by the competition.</p>

                                    <p>We also found that 75 percent more recruiters included the term DevOps in job descriptions between January 2012 and January 2013, compared to the same period a year earlier - an indication that DevOps adoption is accelerating.</p>

